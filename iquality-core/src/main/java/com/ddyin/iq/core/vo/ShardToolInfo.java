package com.ddyin.iq.core.vo;

public class ShardToolInfo {

    private String key;

    private String dbNum;

    private String tableNum;

    private String dbNumRst;

    private String tableNumRst;

    public String getKey() {
        return key;
    }

    public void setKey(String key) {
        this.key = key;
    }

    public String getDbNum() {
        return dbNum;
    }

    public void setDbNum(String dbNum) {
        this.dbNum = dbNum;
    }

    public String getTableNum() {
        return tableNum;
    }

    public void setTableNum(String tableNum) {
        this.tableNum = tableNum;
    }

    public String getDbNumRst() {
        return dbNumRst;
    }

    public void setDbNumRst(String dbNumRst) {
        this.dbNumRst = dbNumRst;
    }

    public String getTableNumRst() {
        return tableNumRst;
    }

    public void setTableNumRst(String tableNumRst) {
        this.tableNumRst = tableNumRst;
    }
}
